DB_NAME = 'salon_services.db'

def setup_database():
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()

    # Создаем таблицу категорий
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS categories (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            slug TEXT UNIQUE NOT NULL,
            title TEXT NOT NULL,
            parent_slug TEXT,
            FOREIGN KEY (parent_slug) REFERENCES categories(slug)
        )
    ''')

    # Создаем таблицу услуг
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS services (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name TEXT NOT NULL,
            price TEXT NOT NULL,
            description TEXT,
            category_slug TEXT NOT NULL,
            FOREIGN KEY (category_slug) REFERENCES categories(slug)
        )
    ''')
    conn.commit()
    conn.close()

def populate_database(data):
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()

    for category_slug, category_data in data.items():
        # Вставляем основную категорию
        cursor.execute("INSERT OR IGNORE INTO categories (slug, title, parent_slug) VALUES (?, ?, ?)",
                       (category_slug, category_data['title'], None))

        if "sub_categories" in category_data:
            for sub_category_slug, sub_category_data in category_data["sub_categories"].items():
                # Вставляем подкатегорию
                cursor.execute("INSERT OR IGNORE INTO categories (slug, title, parent_slug) VALUES (?, ?, ?)",
                               (sub_category_slug, sub_category_data['title'], category_slug))

                # Вставляем услуги для подкатегории
                for service_item in sub_category_data["items"]:
                    cursor.execute("INSERT OR IGNORE INTO services (name, price, description, category_slug) VALUES (?, ?, ?, ?)",
                                   (service_item['name'], service_item['price'], service_item.get('description'), sub_category_slug))
        elif "items" in category_data:
            # Вставляем услуги напрямую под основной категорией (если нет подкатегорий)
            for service_item in category_data["items"]:
                cursor.execute("INSERT OR IGNORE INTO services (name, price, description, category_slug) VALUES (?, ?, ?, ?)",
                               (service_item['name'], service_item['price'], service_item.get('description'), category_slug))
    conn.commit()
    conn.close()

